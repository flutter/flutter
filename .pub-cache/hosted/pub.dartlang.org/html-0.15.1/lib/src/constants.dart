bool isWhitespace(String? char) {
  if (char == null) return false;
  return isWhitespaceCC(char.codeUnitAt(0));
}

bool isWhitespaceCC(int charCode) {
  switch (charCode) {
    case 9: // '\t'
    case newLine: // '\n'
    case 12: // '\f'
    case returnCode: // '\r'
    case 32: // ' '
      return true;
  }
  return false;
}

